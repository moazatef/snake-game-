void movesnake(){
    string sound = "bravoo3aleek.wav";
    Location nexthead =  getnexthead();
    //test my board
    switch(board[nexthead.row][nexthead.col]){
     case EMPTY:
        movesnaketonextlocation(nexthead);
        break;
     case APPLE:
         PlaySound(sound.c_str(),NULL,SND_ASYNC);
        moveandgrowsanke(nexthead);
        generatenextapple();
        break;

     case WALL:
     case SNAKEBODY:
        gameover();
        break;
    }

}

void changesnakedirection(char ch)
{
    switch (ch){
        case KEY_UP: snakedirction=UP;
        break;
        case KEY_DOWN : snakedirction=DOWN;
        break;
        case KEY_RIGHT: snakedirction=RIGHT;
        break;
        case KEY_LEFT: snakedirction=LEFT;
        break;
    }
 }
void checkkeyinput()
{
    char ch ;
    if (kbhit()){
    ch = getch();
    if (ch ==0 ){
        ch = getch();
          changesnakedirection(ch);

    }
    }

}

